    public void loadGuestToTable(){
         dbFunctions.Connect();
         guestList = dbFunctions.getAllGuests();
         
         DefaultTableModel dtm = new DefaultTableModel();
        
            dtm.addColumn("ID");
            dtm.addColumn("Ime");
            dtm.addColumn("Prezime");
            dtm.addColumn("Email");
            dtm.addColumn("Telefon");
            
            Object Client[] = new Object[5];
            
            for(int i = 0; i<guestList.size(); i++){
                Client[0] = guestList.get(i).getID();
                Client[1] = guestList.get(i).getFirstName();
                Client[2] = guestList.get(i).getLastName();
                Client[3] = guestList.get(i).getEmail();
                Client[4] = guestList.get(i).getPhone();
                dtm.addRow(Client);
            }
            
            this.guests_TABLE.setModel(dtm);
    }
    
    public boolean provjeraPassworda(String password) {
        if (password.length() < 8) {
            JOptionPane.showMessageDialog(this, "Šifra mora da sadrži najmanje 8 karaktera.");
            return false;
        }

        boolean hasNumber = false;
        for (char c : password.toCharArray()) {
            if (Character.isDigit(c)) {
                hasNumber = true;
                break;
            }
        }

        if (!hasNumber) {
            JOptionPane.showMessageDialog(this, "Šifra mora da sadrži najmanje jedan broj.");
            return false;
        }

        return true;
    }
    
