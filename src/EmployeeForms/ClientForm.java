    private void update_BTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_update_BTActionPerformed
        int id = 0;
        Object idValue = guests_TABLE.getValueAt(guests_TABLE.getSelectedRow(), 0);
        if(idValue != null){
            id = (int) idValue;
        }

        dbFunctions.Connect();
        Guest guest = dbFunctions.getGuestById(id);

        String ime = ime_TB.getText();
        String prezime = prezime_TB.getText();
        String email = email_TB.getText();
        String phone = telefon_TB.getText();

        if(!ime.isEmpty() && !prezime.isEmpty() && !email.isEmpty() && !phone.isEmpty()){
            guest.setFirstName(ime);
            guest.setLastName(prezime);
            guest.setEmail(email);
            guest.setPhone(phone);

            dbFunctions.Connect();
            if(dbFunctions.updateGuest(guest)){
                JOptionPane.showMessageDialog(this, "Gost uspješno ažruriran.");
                loadGuestToTable();
            }else{
                JOptionPane.showMessageDialog(this, "Došlo je do greške.");
            }
        }else{
            JOptionPane.showMessageDialog(this, "Sva polja moraju biti popunjena.");
        }
    }//GEN-LAST:event_update_BTActionPerformed

