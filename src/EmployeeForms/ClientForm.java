    private void dodaj_BTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dodaj_BTActionPerformed
        String ime = ime_TB.getText();
        String prezime = prezime_TB.getText();
        String email = email_TB.getText();
        String password = sifra_TB.getText();
        String confirmPassword = potvrdiSifru_TB.getText();
        String phone = telefon_TB.getText();
        double balance = 0;
        
        if (!ime.isEmpty() && !prezime.isEmpty() && !email.isEmpty() && !password.isEmpty() && !confirmPassword.isEmpty() && !phone.isEmpty()) {
            if (password.equals(confirmPassword)) {
                if (provjeraPassworda(password)) {
                    Guest guest = new Guest(ime, prezime, email, password, phone, balance);

                    dbFunctions.Connect();
        System.out.println("Minecraft");
                    if (dbFunctions.addGuest(guest)) {
                        JOptionPane.showMessageDialog(this, "Gost uspjesno dodat.");
                        loadGuestToTable();
                    } else {
                        JOptionPane.showMessageDialog(this, "Doslo je do greske.");
                    }
                }
            }
        }
    }//GEN-LAST:event_dodaj_BTActionPerformed

