    public void loadGuestToTable(){
         dbFunctionsClient.Connect();
         guestList = dbFunctionsClient.getAllGuests();
         
         DefaultTableModel dtm = new DefaultTableModel();
        
            dtm.addColumn("ID");
            dtm.addColumn("Ime");
            dtm.addColumn("Prezime");
            dtm.addColumn("Email");
            dtm.addColumn("Telefon");
            dtm.addColumn("Iznos na racunu");
            
            Object Client[] = new Object[6];
            
            for(int i = 0; i<guestList.size(); i++){
                Client[0] = guestList.get(i).getID();
                Client[1] = guestList.get(i).getFirstName();
                Client[2] = guestList.get(i).getLastName();
                Client[3] = guestList.get(i).getEmail();
                Client[4] = guestList.get(i).getPhone();
                Client[5] = guestList.get(i).getBalance();
                dtm.addRow(Client);
            }
            
            this.jTable1.setModel(dtm);
    }
    
