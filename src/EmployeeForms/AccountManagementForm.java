    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int id = 0;
        Object idValue = jTable1.getValueAt(jTable1.getSelectedRow(), 0);
        if(idValue != null){
            id = (int) idValue;
        }
        
        double iznos = Double.parseDouble(iznos_TF.getText());
        
        dbFunctionsClient.Connect();
        Guest guest = dbFunctionsClient.getGuestById(id);
        
        if(!iznos_TF.getText().isEmpty()){
            double noviIznos = guest.getBalance() - iznos;
            if(noviIznos >= 0){
                dbFunctionsClient.Connect();
                guest.setBalance(noviIznos);
                if(dbFunctionsClient.updateGuest(guest)){
                    jLabel4.setText(Double.toString(noviIznos));
                    loadGuestToTable();
                    JOptionPane.showMessageDialog(this, "Uspjesno podignut iznos od: " + iznos + ", " + "novi iznos na racunu je: " + Double.toString(noviIznos));
                }else{
                    JOptionPane.showMessageDialog(this, "Doslo je do greske.");
                }
            }else{
                JOptionPane.showMessageDialog(this, "Nemate dovoljno novca na racunu.");
            }
        }else{
            JOptionPane.showMessageDialog(this, "Morate unijeti iznos.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

